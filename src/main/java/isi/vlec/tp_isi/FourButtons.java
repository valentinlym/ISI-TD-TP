    private enum STATE{
        E1(true, false, false, false),
        E2(false, true, false, false),
        E3(false, false, true, false),
        E4(false, false, false, true);
    
        private boolean cb1Avail, cb2Avail, cb3Avail, cb4Avail;

        private STATE(boolean cb1Avail, boolean cb2Avail, boolean cb3Avail, boolean cb4Avail) {
            this.cb1Avail = cb1Avail;
            this.cb2Avail = cb2Avail;
            this.cb3Avail = cb3Avail;
            this.cb4Avail = cb4Avail;
        }

        public boolean isCb1Avail() {
            return cb1Avail;
        }
        public boolean isCb2Avail() {
            return cb2Avail;
        }
        public boolean isCb3Avail() {
            return cb3Avail;
        }
        public boolean isCb4Avail() {
            return cb4Avail;
        }
    }
    
    private STATE state;
    
