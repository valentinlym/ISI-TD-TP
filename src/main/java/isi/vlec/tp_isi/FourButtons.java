    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        switch (state) {
            case E1:
                state = STATE.E2;
                activateState(state);
                break;
            case E2:
            case E3:
            case E4:
                throw new IllegalStateException("Button 1 cannot be pressed in state " + state);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        switch (state) {
            case E1:
                throw new IllegalStateException("Button 2 cannot be pressed in state " + state);
            case E2:
                state = STATE.E3;
                activateState(state);
                break;
            case E3:
            case E4:
                throw new IllegalStateException("Button 2 cannot be pressed in state " + state);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        switch (state) {
            case E1:
            case E2:
                throw new IllegalStateException("Button 3 cannot be pressed in state " + state);
            case E3:
                state = STATE.E4;
                activateState(state);
                break;
            case E4:
                throw new IllegalStateException("Button 3 cannot be pressed in state " + state);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        switch (state) {
            case E1:
            case E2:
            case E3:
                throw new IllegalStateException("Button 4 cannot be pressed in state " + state);
            case E4:
                state = STATE.E1;
                activateState(state);
                break;
        }
    }//GEN-LAST:event_jButton4ActionPerformed

